function normalizeText(text){
    return text
        .toLowerCase()
        .trim()
        .normalize("NFD")
        .replace(/[\u0300-\u036f]/g, "");
}

function allSearchNames(guest){
    return [guest.name, ...guest.aliases].map(normalizeText);
}

function findSleepPlace(){
    const inputRaw = document.getElementById("sleepInput").value;
    const input = normalizeText(inputRaw);
    const result = document.getElementById("sleepResult");

    if(input === ""){
        result.innerHTML = "Įveskite vardą arba vardą ir pavardę.";
        return;
    }

    const matches = accommodation.filter(guest =>
        allSearchNames(guest).includes(input)
    );

    if(matches.length === 1){
        const person = matches[0];

        result.innerHTML = `
            🏠 Jūsų nakvynės vieta:<br>
            <strong>${person.place}</strong><br>
            <span>${person.room}</span>
        `;
        return;
    }

    if(matches.length > 1){
        result.innerHTML = `
            Vardo neužtenka, nes šventėje yra daugiau nei vienas žmogus tokiu vardu.<br>
            Įveskite vardą ir pavardę.
        `;
        return;
    }

    const partialMatches = accommodation.filter(guest =>
        normalizeText(guest.name).includes(input)
    );

    if(partialMatches.length === 1){
        const person = partialMatches[0];

        result.innerHTML = `
            🏠 Jūsų nakvynės vieta:<br>
            <strong>${person.place}</strong><br>
            <span>${person.room}</span>
        `;
        return;
    }

    if(partialMatches.length > 1){
        result.innerHTML = `
            Rasta keli žmonės pagal šį vardą.<br>
            Įveskite vardą ir pavardę.
        `;
        return;
    }

    result.innerHTML = "Vardas nerastas. Pasitikrinkite, ar įrašėte teisingai.";
}

